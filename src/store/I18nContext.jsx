const LANGUAGE_LABELS = {
    es: 'Español',
    ru: 'Русский',
    ko: '한국어',
};

const I18nContext = createContext(null);

const LANG_STORAGE_KEY = 'organizador_moldes_lang';

export function I18nProvider({ children }) {
    const [lang, setLang] = useState(() => {
        try {
            return localStorage.getItem(LANG_STORAGE_KEY) || 'es';
        } catch { return 'es'; }
    });

    const changeLang = useCallback((newLang) => {
        setLang(newLang);
        try { localStorage.setItem(LANG_STORAGE_KEY, newLang); } catch { }
    }, []);

    const t = useCallback((key, params) => {
        let text = translations[lang]?.[key] || translations.es[key] || key;
        if (params) {
            Object.entries(params).forEach(([k, v]) => {
                text = text.replace(`{${k}}`, v);
            });
        }
        return text;
    }, [lang]);

    return (
        <I18nContext.Provider value={{ lang, changeLang, t, LANGUAGE_LABELS }}>
            {children}
        </I18nContext.Provider>
    );
}

export function useI18n() {
    const ctx = useContext(I18nContext);
    if (!ctx) throw new Error('useI18n debe usarse dentro de I18nProvider');
    return ctx;
}

export default I18nContext;
